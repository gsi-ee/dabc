#include "dabc/eventsapi.h"

// ======================================================================================

unsigned dabc::EventsIterator::NumEvents(const Buffer& buf)
{
   if (!Assign(buf)) return 0;
   unsigned cnt = 0;
   while (NextEvent()) cnt++;
   Close();
   return cnt;
}
